package com.google.javaHomework122;

public class Conditional {
	public static void main(String[] args) {
		float fValue1 = 2;
		float fValue2 = 4;
		
		if((fValue1 == 2) && (fValue2 ==4))
			System.out.println("fValue 2 and 4");
	
		char cValue1 = 8;
		char cValue2 = 9;
		
		if((cValue1 == 8) || (cValue2 == 9))
			System.out.println("cValue 8 or 9");
		
	}
}
